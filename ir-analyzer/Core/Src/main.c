/* Private function prototypes -----------------------------------------------*/
void HAL_GPIO_EXTI_Callback(uint16_t pin) {

  if (GPIO_PIN_RESET == HAL_GPIO_ReadPin(IR_GPIO_Port, IR_Pin) && !recording) {
    recording = 1;
    timer = 0;
    current_pulse = 0;
    return;
  }

  if (recording && current_pulse < MAX_PULSES) {
    pulses[current_pulse] = timer;
    timer = 0;
    current_pulse++;
  }

}

void HAL_TIM_OC_DelayElapsedCallback(TIM_HandleTypeDef *htim) {
  if (recording) {
    timer++;
    if (timer > 1000) {
      recording = 0;
      timer = 0;
      ready = 1;
    }
  }
}

